package eda.pkgfinal;

public class Caballero {
    private String nombre;
    private int poder;

    public Caballero() {
        
    }
    
    public Caballero(String nombre, int poder) {
        this.nombre = nombre;
        this.poder = poder;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getPoder() {
        return poder;
    }

    public void setPoder(int Poder) {
        this.poder = Poder;
    }
    
    
    
}
